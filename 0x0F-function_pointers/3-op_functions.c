#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - function adds
 * @a: first argument
 * @b: second argument
 * Return: a result
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - function substract
* @a: first argument
* @b: second argument
* Return: a result
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - function substract
* @a: first argument
* @b: second argument
* Return: a result
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - function substract
* @a: first argument
* @b: second argument
* Return: a result
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - function substract
* @a: first argument
* @b: second argument
* Return: a result
*/
int op_mod(int a, int b)
{
return (a % b);
}
